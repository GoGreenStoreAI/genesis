# --- 6) Ensure Pages workflow exists (static deploy) ---
mkdir -p .github/workflows
cat > .github/workflows/static.yml << 'YML'
name: Deploy static content to Pages
on:
  push:
    branches: [ "main" ]
permissions:
  contents: read
  pages: write
  id-token: write
concurrency:
  group: "pages"
  cancel-in-progress: true
jobs:
  deploy:
    environment:
      name: github-pages
      url: ${{ steps.deployment.outputs.page_url }}
    runs-on: ubuntu-latest
    steps:
      - name: Checkout
        uses: actions/checkout@v4
      - name: Setup Pages
        uses: actions/configure-pages@v5
      - name: Upload artifact
        uses: actions/upload-pages-artifact@v3
        with:
          path: '.'
      - name: Deploy to GitHub Pages
        id: deployment
        uses: actions/deploy-pages@v4
YML

# --- 7) Commit & push with retries ---
git add -A
git commit -m "Engrave: FREEDOM.md + homepage + Pages workflow $(date -u +%F)" || echo "No changes to commit"
git pull --rebase || true

tries=0
until git push; do
  tries=$((tries+1))
  echo "[engrave] push failed, retry $tries in 5s..."
  sleep 5
  [ $tries -ge 5 ] && { echo "[engrave] giving up for now"; break; }
done

echo "✅ Engraving complete. Pages will update shortly:"
echo "➡  https://gogreenstoreai.github.io/genesis/"
